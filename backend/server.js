app.use('/api/grades', authenticateToken, require('./routes/grades'));
app.use('/api/attendance', authenticateToken, require('./routes/attendance'));
app.use('/api/exams', authenticateToken, require('./routes/exams'));
app.use('/api/fees', authenticateToken, require('./routes/fees'));
app.use('/api/announcements', authenticateToken, require('./routes/announcements'));
app.use('/api/notifications', authenticateToken, require('./routes/notifications'));
app.use('/api/chatbot', authenticateToken, require('./routes/chatbot'));
app.use('/api/academics', authenticateToken, require('./routes/academics'));
app.use('/api/reports', authenticateToken, require('./routes/reports'));
app.use('/api/communication', authenticateToken, require('./routes/communication'));
